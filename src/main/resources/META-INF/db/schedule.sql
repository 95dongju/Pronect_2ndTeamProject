CREATE TABLE STD_SCHEDULE (
    SCID NUMBER(10) PRIMARY KEY,
    SID NUMBER(5),
    MID VARCHAR2(100) NOT NULL, 
    SCTITLE VARCHAR2(200) NOT NULL,
    SCMEMO VARCHAR2(500),
    SCSTART DATE NOT NULL,
    SCEND DATE,
    FOREIGN KEY (SID) REFERENCES STUDY (SID),
    FOREIGN KEY (MID) REFERENCES MEMBER (MID)
);